#DATA STRUCTURES/CORE DATA TYPES:


#LIST:
a=[10,20,30,40,50]
b=list((10,20,30,40,50))
c=[]
print(type(a))
print(type(b))
print(type(c))
print(a)
print(b)
print(c)


#TUPLES:
a=10,20,30,40,50
b=(10,20,30,40,50)
c=tuple([10,20,30,40,50])
d=()
e=10,
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(a)
print(b)
print(c)
print(d)
print(e)




#SET:
a={10,20,30,40,50}
b=set({10,20,30,40,50})
c=set({})
print(type(a))
print(type(b))
print(type(c))
print(a)
print(b)
print(c)


